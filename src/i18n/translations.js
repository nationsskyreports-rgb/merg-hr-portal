/**
 * Get greeting based on time of day
 */
export const getGreeting = (lang) => {
  const hour = new Date().getHours();
  
  if (lang === 'ar') {
    return hour < 12 ? translations.ar.good_morning : translations.ar.good_evening;
  }
  
  if (hour < 12) return translations.en.good_morning;
  if (hour < 17) return translations.en.good_afternoon;
  return translations.en.good_evening;
};

